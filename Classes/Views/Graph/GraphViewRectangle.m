#import "GraphViewRectangle.h"

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
@interface GraphViewRectangle (PrivateAPI)

@end

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation GraphViewRectangle

//-----------------------------------------------------------------------------------------------------------------------------------
@synthesize xRange;
@synthesize yRange;
@synthesize rectColor;

//===================================================================================================================================
#pragma mark GraphViewRectangle

//-----------------------------------------------------------------------------------------------------------------------------------
- (id)initWithXRange:(GraphViewDataRange)initXRange andYRange:(GraphViewDataRange)initYRange  {
    if (self = [super init]) {
        self.xRange = initXRange;
        self.yRange = initYRange;
        self.rectColor = [UIColor colorWithWhite:0.0f alpha:1.0f];
    }
    return self;
}

//===================================================================================================================================
#pragma mark GraphViewRectangle PrivateAPI

//===================================================================================================================================
#pragma mark NSObject Methods

//-----------------------------------------------------------------------------------------------------------------------------------
- (void)dealloc {
    [super dealloc];
}

@end
