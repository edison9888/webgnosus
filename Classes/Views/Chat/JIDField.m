//-----------------------------------------------------------------------------------------------------------------------------------
- (BOOL)isValidJID {
    BOOL isValid = NO;
    if (self.text) {
        NSArray* splitJid = [self.text componentsSeparatedByString:@"@"];
        if ([splitJid count] == 2) {
            isValid = YES;
        }
    } else {
        isValid = YES;
    }
    return isValid;
}

